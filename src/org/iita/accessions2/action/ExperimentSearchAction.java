package org.iita.accessions2.action;

import org.iita.accessions2.model.Experiment;
import org.iita.service.SearchService;

/**
 * @author mobreza
 *
 */
@SuppressWarnings("serial")
public class ExperimentSearchAction extends org.iita.struts.SearchAction<Experiment> {

	/**
	 * @param searchService
	 */
	public ExperimentSearchAction(SearchService<Object> searchService) {
		super(searchService);
	}

	/**
	 * @see org.iita.struts.SearchAction#getSearchedClass()
	 */
	@Override
	protected Class<?> getSearchedClass() {
		return Experiment.class;
	}

}
